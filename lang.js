// Function to apply translations
function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (t[key]) {
      // For title tag
      if (element.tagName === 'TITLE') {
        element.textContent = t[key];
      } else {
        element.textContent = t[key];
      }
    }
  });

  // Update all select options with data-i18n attribute
  document.querySelectorAll('select [data-i18n]').forEach(option => {
    const key = option.getAttribute('data-i18n');
    let langKey = key;
    
    if (lang === 'es') {
      langKey = key + '_es';
    } else if (lang === 'en') {
      langKey = key + '_en';
    }
    
    if (t[langKey]) {
      option.textContent = t[langKey];
      option.value = t[langKey]; // Also update the value to match the display text
    }
  });

  // Update all elements with data-i18n-placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (t[key]) {
      element.placeholder = t[key];
    }
  });

  // Update html lang attribute
  document.documentElement.lang = lang;
}

// Function to set language
function setLanguage(lang) {
  console.log(`[DEBUG] setLanguage called with: ${lang}`);
  
  // Save to localStorage
  localStorage.setItem('preferredLanguage', lang);
  
  // Apply translations
  applyTranslations(lang);
}

